#2.Сумма положительных чисел
#____________________________________________________________________
numbers = [1245.4435, -302.1403, 87459.99, -520.8265, 1450.001]
sum = 0

for i in numbers:
    if i > 0:
        sum += i
print(f"${sum:,.2f}")

