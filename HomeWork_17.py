#1. Проверка на подмножество
#____________________________________________________________________
set1 = {1, 2, 3, 4}
set2 = {2, 3}
#v.1.1
print(set1 >= set2)
print(set2 <= set1)
#v.1.2
print(set2.issubset(set1))
print(set1.issuperset(set2))
#v.2.1
flag = True
for item in set2:
    if item not in set1:
        flag = False
        break
print(flag)
