#2. Зеркальное подмножество
#____________________________________________________________________
set1 = {2, 3, 4, 5, 6}
set2 = {4, 5}
if set2 >= set1:
    print("Подмножество:", True)
    print("Разница:", set2 - set1)
elif set1 >= set2:
    print("Подмножество:", True)
    print("Разница:", set1 - set2)
else:
    print("Подмножество:", False)

